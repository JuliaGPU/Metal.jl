	function getbench(dict, benches::AbstractVector)
		_dict = _getbench(dict)
		for b in benches
			_dict = _getbench(_dict, b)
		end
		return _dict["time"]
	end
	function _getbench(dict)
		dict[2][1][2]["data"]["array"][2]
	end
	function _getbench(dict, bench)
		dict["data"][bench][2]
	end
end

